def get_headers(referer=None,cookies=None):
	'''
	生成随机请求头
	：params referer:请求来源
	：params cookies:cookies
	：return headers:随机请求头
	'''
	headers = {
	'User-Agent':random.choice(USER_AGENTS),
	'Accept':'text/html,application/xhtml+xml,application/xml;q=0.9,image/webp,image/apng,*/*;q=0.8,application/signed-exchange;v=b3',
	'Accept-language':'zh-CN,zh;q=0.9,en;q=0.8',
    'Referer':referer,
	#'Accept-Encoding':'gzip, deflate,br',
	'Connection':'keep-alive',
	'Cookies':cookies,
	}

	return headers


def file_download(url,fname='',write_mode=None,download_path='./',stream=True,timeout=None):
	'''
	文件下载
	：params url:文件链接
	：params fname:文件名
	：params download_path:下载路径,默认为当前路径
	：params write_mode:写入模式
	：params 
	'''
	file_path = download_path + fname
	with closing(requests.get(url,headers=get_headers(),stream=stream,verify=False,timeout=timeout)) as res:
				with open(file_path,write_mode) as f:
					print('*'*50)
					print('[{}] 开始下载'.format(fname))
					for chunk in res.iter_content(chunk_size = 1024):
						f.write(chunk)
						print('\r请稍等......',end='')
					print('\n[{}] 下载完成'.format(fname))
					print('*'*50)


